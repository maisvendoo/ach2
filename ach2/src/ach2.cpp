ACH2::~ACH2()
{

}

//------------------------------------------------------------------------------
//
//------------------------------------------------------------------------------
void ACH2::initialization()
{
    initControlCircuit();

    initSounds();
}

//------------------------------------------------------------------------------
//
//------------------------------------------------------------------------------
void ACH2::step(double t, double dt)
{
    // Работа цепей управления
    stepControlCircuit(t, dt);

    // Вывод сигналов в кабину и внешнюю модель
    stepSignalsOutput(t, dt);
}

