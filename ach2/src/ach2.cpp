ACH2::ACH2(QObject *parent) : Vehicle(parent)
  , battery(Q_NULLPTR)
  , Ucc(0.0)
  , Icc(0.0)
  , fuel_tank(Q_NULLPTR)
{

}

//------------------------------------------------------------------------------
